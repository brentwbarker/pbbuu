      FUNCTION RANG(ISEED)
      use global
C NORMAL DISTRIBUTION GENERATOR
C  ISEED SHOULD BE SET ODD POSITIVE IN THE RANGE 0, 2 147 483 647
C  AT LEAST 5 DECIMAL DIGITS
      INTEGER*4 ISEED
      REAL*8 R,PI2,FI,AG
      PARAMETER(PI2= 6.2831 85307 17958 64769 25287 D0)
C
 10   CONTINUE
      AG=1D0-DBLE(getRan())
      IF(AG.LT.1D-15)GOTO 10
      R=DSQRT(-2.D0*DLOG(AG))
      FI=PI2*getRan()
      RANG=R*DCOS(FI)
C
      END
